format long
G1=1/600;
G2=1/600;
G3=1/1200;
C1=7.115e-9;
C2=1.881e-9;
C3=9.538e-9;
C4=4.067e-9;
C5=37.123e-9;
L1=6.084e-3;
L2=23.018e-3;
L3=4.6256e-3;
L4=10.649e-3;
L5=1.166e-3;
A=zeros(13,13);
SA=zeros(1,30000);
B=zeros(13,1);
