for i = 10000:1:39999
    S=(2 .* pi .*f .* 1i);
    SL1=S.*L1;
    SL2=S.*L2;
    SL3=S.*L3;
    SL4=S.*L4;
    SL5=S.*L5;
    SC1=S.*C1;
    SC2=S.*C2;
    SC3=S.*C3;
    SC4=S.*C4;
    SC5=S.*C5;
    %Impedance Matrix
    A(1,1)=G1;
    A(2,1)=-G1;
    A(1,2)=-G1;
    A(2,2)=G1+1/SL1;
    A(3,2)=1/SL1;
    A(3,3)=1/SL1+SC1;
    A(4,3)=-SC1;
    A(3,4)=-SC1;
    A(4,4)=SC1+1/SL2+1/SL4;
    A(7,4)=-1/SL2;
    A(4,7)=-1/SL2;
    A(5,5)=SC4+1/SL4;
    A(5,4)=-1/SL4;
    A(4,5)=-1/SL4;
    A(6,6)=SC4+SC5+1/SL5;
    A(6,5)=-SC4;
    A(5,6)=-SC4;
    A(7,7)=SC2+1/SL2;
    A(8,7)=-SC2;
    A(7,8)=-SC2;
    A(9,9)=G3;
    A(10,9)=-G3;
    A(9,10)=-G3;
    A(10,10)=G3;
    %A(9,6)=-G2;
    %A(6,9)=-G2;
    A(13,1)=1;
    A(1,13)=1;
    %Voltage Source
    A(13,1)=1;
    A(1,13)=1;
    %Current through Resistor R2
    A(12,6)=1;
    A(12,9)=-1;
    A(6,12)=1;
    A(9,12)=-1;
    A(12,12)=-1/G1;
    %Op-Amp
    A(11,9)=1;
    A(10,11)=1;
    A(10,11)=-1/1000;
    A(11,10)=-1/1000;
    x=A\B;
    SA(f-9999)=x(11);
end
